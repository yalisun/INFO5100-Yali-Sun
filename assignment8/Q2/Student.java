package assignment8.Q2;

public class Student {

    private String id;
    private String name;
    private String gender;
    private String age;

    public Student(String id, String name, String age, String gender) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.gender = gender;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName() {
        this.name = name;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String toString() {
        return "[Id, Name, Gender, Age] = [" + getId() + "," + getName() + "," + getGender() + "," + getAge() + "]";
    }
}
